    private void cboJabatanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboJabatanActionPerformed
if(cboJabatan.getSelectedItem()=="Kepala Devisi")
{
    gaji = 5000000;
    pph = 350000;
    txtGaji.setText(num.format(gaji));
    txtPajak.setText(num.format(pph));
}
else if(cboJabatan.getSelectedItem()=="Staff")
{
    gaji = 3500000;
    pph = 245000;
    txtGaji.setText(num.format(gaji));
    txtPajak.setText(num.format(pph));
}
else if(cboJabatan.getSelectedItem()=="Karyawan")
{
    gaji = 2000000;
    pph = 140000;
    txtGaji.setText(num.format(gaji));
    txtPajak.setText(num.format(pph));
}// TODO add your handling code here:
    }//GEN-LAST:event_cboJabatanActionPerformed

    private void cboStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboStatusActionPerformed
if("Menikah".equals(cboStatus.getSelectedItem()))
{
    tk = gaji*15/100;
    txtTanak.enable();
    cboAnak.enable();
}
else if("Single".equals(cboStatus.getSelectedItem()))
{
    tk = 0;
    txtTanak.setText("");
    txtTanak.disable();
    cboAnak.disable();
}
txtTkeluarga.setText(num.format(tk));// TODO add your handling code here:
    }//GEN-LAST:event_cboStatusActionPerformed

    private void cboAnakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboAnakActionPerformed
if("0".equals(cboAnak.getSelectedItem()))
{
    ta = 0;
    txtTanak.setText(num.format(ta));
}
else if("1".equals(cboAnak.getSelectedItem()))
{
    ta = gaji*5/100;
    txtTanak.setText(num.format(ta));
}
else if("2".equals(cboAnak.getSelectedItem()))
{
    ta = gaji*10/100;
    txtTanak.setText(num.format(ta));
}
else if("Lebih dari 2".equals(cboAnak.getSelectedItem()))
{
    ta = gaji*15/100;
    txtTanak.setText(num.format(ta));
}       // TODO add your handling code here:
    }//GEN-LAST:event_cboAnakActionPerformed

