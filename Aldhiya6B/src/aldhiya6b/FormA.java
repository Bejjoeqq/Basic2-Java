    private void cboKodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboKodeActionPerformed
int jenis = cboKode.getSelectedIndex();
switch(jenis)
{
    case 0 : txtNama.setText("Andi");
    txtBagian.setText("Pemasaran");break;
    case 1 : txtNama.setText("Budi");
    txtBagian.setText("Personalia");break;
    case 2 : txtNama.setText("Cici");
    txtBagian.setText("Keuangan");break;
    case 3 : txtNama.setText("Dodi");
    txtBagian.setText("Gudang");break;
    default : break;
}// TODO add your handling code here:
    }//GEN-LAST:event_cboKodeActionPerformed
